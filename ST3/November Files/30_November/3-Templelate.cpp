#include <bits/stdc++.h>


using namespace std;

template<typename T>
void display(T a, T b) {
    cout << a << " " << b << endl;
}

int main() {
    int a = 10;
    int b = 20;

    display(a,b);

    char c = 'c';
    char d = 'd';

    display(c,d);

    return 0;
}