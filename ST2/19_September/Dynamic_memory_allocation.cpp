/* STATIC MEMORY ALLOCATION => SMA */
/**
 * Stack Memory is Used
 * Smaller in size 10 ^ 6
 * Memory is allocated Compiler Time
 * Programmer does not need to deallocate memory after usage
 */

/* DYNAMIC MEMORY ALLOCATION => DMA */
/**
 * Heap Memory is Used
 * Larger is Size
 * Memory is allocated at run time
 * Programmer needs to deallocate memory After usage
 */
