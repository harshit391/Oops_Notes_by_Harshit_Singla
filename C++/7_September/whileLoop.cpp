#include <iostream>

using namespace std;

int main()
{
    // Initialization
    int i = 1;

    // So it is simple english
    // That while our condition is true
    // Execute the block

    while (i <= 10 /* Condition for the initialized variable to end the loop till this condition get false */)
    {   
        // So this hello world get printed 10 time
        cout << "Hello World!" << endl;

        // Updation of variable we are using to continue the loop
        i++;
    } 

}